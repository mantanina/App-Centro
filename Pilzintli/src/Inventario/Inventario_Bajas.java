    private void eliminarproductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarproductoActionPerformed
       Datos_Inventario miinventario = new Datos_Inventario();
       InventarioDAO daoinventario = new InventarioDAO();
       
       miinventario.setId_inventario(Integer.parseInt(Codigodelproducto.getText()));
       daoinventario.EliminarInventario(miinventario);
    }//GEN-LAST:event_eliminarproductoActionPerformed
    private void llenarinventario(){
        InventarioDAO dao = new InventarioDAO();
        ArrayList<Datos_Inventario> lista = dao.ConsultarInventario();
        if(lista.size()>0){
            for (int i = 0; i < lista.size(); i++) {
                fila[0]= lista.get(i).getId_inventario();
                fila[1]= lista.get(i).getNombre_articulo();
                fila[2]= lista.get(i).getCantidad_disponible();
                fila[3]= lista.get(i).getFecha_entrada();
                fila[4]= lista.get(i).getFecha_salida();
                
                modelo.addRow(fila);
            }
        }
    }
